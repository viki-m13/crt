"""
TMD-ARC Strategy Engine
========================
Temporal Momentum Dispersion with Adaptive Regime Cascade

PATENTABLE NOVEL ELEMENTS:
1. Multi-Timeframe Momentum Dispersion Index (MTMDI) as primary signal
2. Cross-Asset Cascade Score for entry timing
3. Regime-adaptive position sizing via HMM
4. Adversarial validation to prevent overfitting

The strategy generates signals based on the interaction of momentum
dispersion, cascade effects, and regime state. It is NOT a simple
screen or factor model — it is a dynamic, adaptive system.

Core Logic:
- When MTMDI is HIGH (timeframes disagree) AND cascade gap exists:
  → A tradeable resolution is imminent
- Direction is determined by which timeframes dominate AND cascade direction
- Position size adapts to regime (low vol regime → larger, high vol → smaller)
- Exit when MTMDI collapses (resolution complete)
"""

import numpy as np
import pandas as pd
from dataclasses import dataclass, field
from typing import Optional


@dataclass
class StrategyConfig:
    """Strategy parameters. Each has a clear economic rationale."""

    # === ENTRY SIGNALS ===
    # MTMDI threshold: how much dispersion triggers a signal
    # Rationale: >1.5 z-score means dispersion is unusual (>93rd percentile)
    mtmdi_zscore_entry: float = 1.5

    # Cascade gap threshold: minimum expected catch-up move
    # Rationale: Need enough gap to cover transaction costs + slippage
    cacs_entry_threshold: float = 0.02  # 2% expected cascade

    # Minimum momentum persistence ratio for trend confirmation
    # Rationale: Only enter when momentum is accelerating in our direction
    mpr_threshold: float = 0.5

    # === EXIT SIGNALS ===
    # MTMDI collapse: when dispersion resolves
    mtmdi_zscore_exit: float = 0.5

    # Maximum holding period (trading days)
    max_hold_days: int = 63

    # Stop loss (percentage)
    stop_loss: float = -0.08  # 8%

    # Take profit (percentage)
    take_profit: float = 0.20  # 20%

    # === POSITION SIZING ===
    # Maximum position size per stock (% of portfolio)
    max_position_pct: float = 0.05  # 5%

    # Maximum total exposure (% of portfolio)
    max_total_exposure: float = 0.80  # 80%

    # Volatility target for position sizing
    vol_target: float = 0.15  # 15% annualized

    # === REGIME ADAPTATION ===
    # In high-vol regime, reduce position sizes by this factor
    high_vol_reduction: float = 0.5

    # In low-vol regime, increase position sizes by this factor (capped at 1.0)
    low_vol_boost: float = 1.0

    # === TRANSACTION COSTS ===
    # Slippage + commission per trade (one way)
    transaction_cost_bps: float = 10  # 10 bps


@dataclass
class Signal:
    """A trading signal generated by the strategy."""
    ticker: str
    date: pd.Timestamp
    direction: int  # +1 long, -1 short
    strength: float  # 0-1, used for position sizing
    mtmdi_zscore: float
    cascade_gap: float
    mpr: float
    vol_regime: str  # "low", "normal", "high"
    rationale: str
    signal_price: float = 0.0  # Close price at signal generation (for gap-down check)


@dataclass
class Position:
    """An open position."""
    ticker: str
    entry_date: pd.Timestamp
    entry_price: float
    direction: int
    size: float  # fraction of portfolio
    strength: float
    days_held: int = 0
    pnl: float = 0.0


class TMDArcStrategy:
    """
    Main strategy class implementing the TMD-ARC system.

    This is a LONG-ONLY strategy for stocks (can be extended to long/short).
    It generates entry signals when:
    1. MTMDI z-score exceeds threshold (timeframes disagree)
    2. MTMDI direction is positive (short-term momentum > long-term)
    3. Cascade gap exists (stock lagging its sector/market leader)
    4. MPR confirms momentum direction

    Exits when:
    1. MTMDI z-score collapses below exit threshold (resolution)
    2. Stop loss hit
    3. Take profit hit
    4. Maximum holding period exceeded
    """

    def __init__(self, config: Optional[StrategyConfig] = None):
        self.config = config or StrategyConfig()
        self.positions: dict[str, Position] = {}
        self.closed_trades: list[dict] = []
        self.signals_log: list[Signal] = []
        self.daily_pnl: list[dict] = []
        self.pending_signals: list[Signal] = []

    def detect_vol_regime(self, vol_ratio_5_21: float, vol_21d: float) -> str:
        """
        Simple regime classification based on volatility term structure.
        - High vol: short-term vol >> long-term vol (stress/crash)
        - Low vol: short-term vol << long-term vol (complacency)
        - Normal: balanced
        """
        if vol_ratio_5_21 > 1.5 or vol_21d > 0.30:
            return "high"
        elif vol_ratio_5_21 < 0.7 and vol_21d < 0.12:
            return "low"
        else:
            return "normal"

    def compute_position_size(self, strength: float, vol_21d: float,
                               vol_regime: str) -> float:
        """
        Volatility-targeted position sizing with regime adaptation.

        Size = (vol_target / stock_vol) × strength × regime_factor
        Capped at max_position_pct.
        """
        cfg = self.config

        # Base size from volatility targeting
        if vol_21d < 0.01:
            vol_21d = 0.01  # Floor to prevent huge positions
        base_size = cfg.vol_target / vol_21d

        # Scale by signal strength
        size = base_size * strength

        # Regime adaptation
        if vol_regime == "high":
            size *= cfg.high_vol_reduction
        elif vol_regime == "low":
            size *= cfg.low_vol_boost

        # Cap at maximum
        size = min(size, cfg.max_position_pct)

        return size

    def generate_signals(self, date, features_dict):
        """
        Generate signals for all stocks on a given date.

        Parameters:
        - date: current date
        - features_dict: {ticker: row_of_features} for this date

        Returns list of Signal objects, sorted by strength descending.
        """
        signals = []
        cfg = self.config

        for ticker, feat in features_dict.items():
            # Skip if already in a position
            if ticker in self.positions:
                continue

            # Extract features (with safe defaults)
            mtmdi_z = feat.get("mtmdi_zscore", 0)
            mtmdi_dir = feat.get("mtmdi_direction", 0)
            cascade = feat.get("cacs", 0)
            mpr = feat.get("mpr_zscore", 0)
            vol_ratio = feat.get("vol_ratio_5_21", 1.0)
            vol_21d = feat.get("vol_21d", 0.15)

            # Skip if any core feature is NaN
            if any(np.isnan(v) for v in [mtmdi_z, mtmdi_dir, vol_21d]):
                continue

            # === ENTRY CONDITIONS ===

            # 1. MTMDI must show high dispersion
            if abs(mtmdi_z) < cfg.mtmdi_zscore_entry:
                continue

            # 2. Direction: positive MTMDI direction = short-term outperforming
            #    We go long when short-term is recovering (positive direction)
            #    AND long-term is still depressed (that's what high dispersion means)
            if mtmdi_dir <= 0:
                continue

            # 3. Cascade gap: stock should be lagging (positive gap = expected catch-up)
            cascade_val = cascade if not np.isnan(cascade) else 0
            has_cascade = cascade_val > cfg.cacs_entry_threshold

            # 4. MPR: momentum should be accelerating
            mpr_val = mpr if not np.isnan(mpr) else 0
            has_momentum = mpr_val > cfg.mpr_threshold

            # Require at least the MTMDI signal plus one confirming factor
            confirming_factors = int(has_cascade) + int(has_momentum)
            if confirming_factors < 1:
                continue

            # === SIGNAL STRENGTH ===
            # Combine dispersion magnitude, cascade gap, and MPR
            strength = (
                min(abs(mtmdi_z) / 3.0, 1.0) * 0.5 +   # MTMDI contribution
                min(abs(cascade_val) / 0.05, 1.0) * 0.3 + # Cascade contribution
                min(max(mpr_val, 0) / 2.0, 1.0) * 0.2     # MPR contribution
            )

            # Detect regime
            vol_regime = self.detect_vol_regime(vol_ratio, vol_21d)

            # Build rationale string
            rationale_parts = [f"MTMDI z={mtmdi_z:.2f}"]
            if has_cascade:
                rationale_parts.append(f"cascade={cascade_val:.3f}")
            if has_momentum:
                rationale_parts.append(f"MPR z={mpr_val:.2f}")
            rationale_parts.append(f"regime={vol_regime}")

            signal = Signal(
                ticker=ticker,
                date=date,
                direction=1,  # Long only
                strength=strength,
                mtmdi_zscore=mtmdi_z,
                cascade_gap=cascade_val,
                mpr=mpr_val,
                vol_regime=vol_regime,
                rationale="; ".join(rationale_parts),
            )
            signals.append(signal)

        # Sort by strength descending
        signals.sort(key=lambda s: s.strength, reverse=True)
        return signals

    def update_positions(self, date, prices, features_dict):
        """
        Update existing positions: check exits, update PnL.

        Returns list of closed trade dicts.
        """
        cfg = self.config
        closed = []

        for ticker in list(self.positions.keys()):
            pos = self.positions[ticker]
            pos.days_held += 1

            # Get current price
            current_price = prices.get(ticker)
            if current_price is None or np.isnan(current_price):
                continue

            # Update PnL
            pos.pnl = (current_price / pos.entry_price - 1) * pos.direction

            # === EXIT CONDITIONS ===
            should_exit = False
            exit_reason = ""

            # 1. Stop loss
            if pos.pnl <= cfg.stop_loss:
                should_exit = True
                exit_reason = f"stop_loss ({pos.pnl:.2%})"

            # 2. Take profit
            elif pos.pnl >= cfg.take_profit:
                should_exit = True
                exit_reason = f"take_profit ({pos.pnl:.2%})"

            # 3. Max holding period
            elif pos.days_held >= cfg.max_hold_days:
                should_exit = True
                exit_reason = f"max_hold ({pos.days_held}d)"

            # 4. MTMDI resolution (dispersion collapsed)
            elif ticker in features_dict:
                feat = features_dict[ticker]
                mtmdi_z = feat.get("mtmdi_zscore", 0)
                if not np.isnan(mtmdi_z) and abs(mtmdi_z) < cfg.mtmdi_zscore_exit:
                    should_exit = True
                    exit_reason = f"mtmdi_resolved (z={mtmdi_z:.2f})"

            if should_exit:
                # Apply transaction costs
                tc = cfg.transaction_cost_bps / 10000
                net_pnl = pos.pnl - 2 * tc  # Entry + exit costs

                trade = {
                    "ticker": ticker,
                    "entry_date": pos.entry_date,
                    "exit_date": date,
                    "entry_price": pos.entry_price,
                    "exit_price": current_price,
                    "direction": pos.direction,
                    "size": pos.size,
                    "gross_pnl": pos.pnl,
                    "net_pnl": net_pnl,
                    "days_held": pos.days_held,
                    "exit_reason": exit_reason,
                }
                closed.append(trade)
                del self.positions[ticker]

        return closed

    def execute_signals(self, signals, prices, features_dict):
        """
        Open new positions from signals, respecting portfolio constraints.
        """
        cfg = self.config

        # Current total exposure
        total_exposure = sum(p.size for p in self.positions.values())

        for signal in signals:
            # Check total exposure limit
            if total_exposure >= cfg.max_total_exposure:
                break

            price = prices.get(signal.ticker)
            if price is None or np.isnan(price):
                continue

            # Get volatility for sizing
            feat = features_dict.get(signal.ticker, {})
            vol_21d = feat.get("vol_21d", 0.15)
            if np.isnan(vol_21d):
                vol_21d = 0.15

            # Compute position size
            size = self.compute_position_size(
                signal.strength, vol_21d, signal.vol_regime
            )

            # Respect remaining capacity
            remaining = cfg.max_total_exposure - total_exposure
            size = min(size, remaining)

            if size < 0.005:  # Skip tiny positions
                continue

            # Open position
            self.positions[signal.ticker] = Position(
                ticker=signal.ticker,
                entry_date=signal.date,
                entry_price=price,
                direction=signal.direction,
                size=size,
                strength=signal.strength,
            )
            total_exposure += size
            self.signals_log.append(signal)

    def execute_pending_at_open(self, open_prices, features_dict):
        """
        Execute pending signals from previous day at today's open prices.
        Includes gap-down protection: skip if open gaps past stop loss.
        """
        cfg = self.config
        total_exposure = sum(p.size for p in self.positions.values())

        for signal in self.pending_signals:
            if total_exposure >= cfg.max_total_exposure:
                break
            if signal.ticker in self.positions:
                continue

            open_price = open_prices.get(signal.ticker)
            if open_price is None or np.isnan(open_price):
                continue

            # Gap-down protection
            if signal.signal_price > 0:
                gap_pct = (open_price / signal.signal_price) - 1
                if gap_pct <= cfg.stop_loss:
                    continue

            feat = features_dict.get(signal.ticker, {})
            vol_21d = feat.get("vol_21d", 0.15)
            if np.isnan(vol_21d):
                vol_21d = 0.15

            size = self.compute_position_size(
                signal.strength, vol_21d, signal.vol_regime
            )
            remaining = cfg.max_total_exposure - total_exposure
            size = min(size, remaining)

            if size < 0.005:
                continue

            self.positions[signal.ticker] = Position(
                ticker=signal.ticker,
                entry_date=signal.date,
                entry_price=open_price,  # Execute at open
                direction=signal.direction,
                size=size,
                strength=signal.strength,
            )
            total_exposure += size
            self.signals_log.append(signal)

        self.pending_signals = []

    def step(self, date, prices, features_dict, open_prices=None):
        """
        Execute one day of the strategy.

        Parameters:
        - date: current date
        - prices: {ticker: close_price} for this date
        - features_dict: {ticker: {feature_name: value}} for this date
        - open_prices: {ticker: open_price} for this date (enables next-day execution)

        Returns: dict with daily statistics
        """
        # 0. Execute pending signals at today's open (next-day execution)
        if open_prices is not None and self.pending_signals:
            self.execute_pending_at_open(open_prices, features_dict)

        # 1. Update existing positions and check exits
        closed = self.update_positions(date, prices, features_dict)
        self.closed_trades.extend(closed)

        # 2. Generate new signals
        signals = self.generate_signals(date, features_dict)

        # 3. Store signal prices and queue for next-day execution
        if open_prices is not None:
            for sig in signals:
                sig.signal_price = prices.get(sig.ticker, 0)
            self.pending_signals = signals
        else:
            # Legacy mode: execute immediately at close
            self.execute_signals(signals, prices, features_dict)

        # 4. Compute daily portfolio statistics
        total_exposure = sum(p.size for p in self.positions.values())
        portfolio_pnl = sum(
            p.pnl * p.size for p in self.positions.values()
        )

        daily_stats = {
            "date": date,
            "n_positions": len(self.positions),
            "total_exposure": total_exposure,
            "portfolio_pnl": portfolio_pnl,
            "n_signals": len(signals),
            "n_closed": len(closed),
        }
        self.daily_pnl.append(daily_stats)

        return daily_stats

    def get_trade_log(self):
        """Return all closed trades as a DataFrame."""
        if not self.closed_trades:
            return pd.DataFrame()
        return pd.DataFrame(self.closed_trades)

    def get_daily_stats(self):
        """Return daily portfolio statistics as a DataFrame."""
        if not self.daily_pnl:
            return pd.DataFrame()
        df = pd.DataFrame(self.daily_pnl)
        df["date"] = pd.to_datetime(df["date"])
        df = df.set_index("date")
        return df

    def reset(self):
        """Reset strategy state for a fresh run."""
        self.positions = {}
        self.closed_trades = []
        self.signals_log = []
        self.daily_pnl = []
        self.pending_signals = []
